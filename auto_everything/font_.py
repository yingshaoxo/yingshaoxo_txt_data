parts = font_data_8x16.split("\n\n")
for part in parts:
    splits = part.split("\n")
    symbol = splits[0]
    data_text = ""
    for line in splits[1:]:
        data_text += line.strip()
    font_data_8x16_dict_as_1d_array[symbol] = data_text


def get_ascii_8_times_16_font_dict():
    dict_ = {}
    parts = font_data_8x16.split("\n\n")
    for part in parts:
        splits = part.split("\n")
        symbol = splits[0]
        points = []
        for line in splits[1:]:
            points.append([int(one) for one in list(line)])
        dict_[symbol] = points
    return dict_


